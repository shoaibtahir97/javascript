/*
When you'r writing strings you might write words that are in inverted comma, 
because you have used double quotes to declare string if you use it again 
than the program may give error. You can solve this problem by typing backslash
before inverted comma,  
*/


var a = "Hello\'World"; //single quote

var b = "Hello\"World"; //double quote

var c = "Hello\\World"; //backslash

var d = "Hello\nWorld"; //New Line

var e = "Hello\tWorld"; //Horizontal Tab (four character space) 